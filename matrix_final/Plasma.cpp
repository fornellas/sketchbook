void Plasma::fillPlasma(byte mode) {
  for(word p=0;p<display->pixels;p++) {
    switch(currMode){
    case 0:
      plasma[p]=pgm_read_word_near(radial+p);
      break;
    case 1:
      plasma[p]=pgm_read_word_near(cone+p);
      break;
    case 2:
      plasma[p]=pgm_read_word_near(spiral+p);
      break;
    case 3:
      plasma[p]=pgm_read_word_near(cones+p);
      break;
    }
  }
}

void Plasma::pSpeedVal(){
  if(pSpeed==0)
    pSpeed++;
  if(pSpeed>6)
    pSpeed=-6;
  if(pSpeed<-6)
    pSpeed=-6;
}

void Plasma::loop() {
  display->fill(BLACK);

  // change mode if MODE button is pressed
  if(button->pressed(A)) {
    currMode++;
    if(currMode>=PLASMA_MODES)
      currMode=0;
    EEPROM.write(EEPROM_PLASMA_MODE, currMode);
    fillPlasma(currMode);
  }

  if(button->pressed(B)) {
    pSpeed++;
    pSpeedVal();
    EEPROM.write(EEPROM_PLASMA_SPEED, pSpeed);
  }

  for(byte p=0;p<display->pixels;p++)
    plasma[p]-=pSpeed;
  for(byte p=0;p<display->pixels;p++)
    display->frameBuff[p]=pgm_read_word_near(spectrum+int(float(plasma[p])/255.0*float(SPECTRUM_LEN-1)));

  display->show();
}

void Plasma::enter() {
  plasma=(byte *)malloc((size_t)(display->pixels)*sizeof(byte));
  fillPlasma(currMode); 
}

void Plasma::exit() {
  display->fill(BLACK);
  display->show();
  free(plasma);
}
















